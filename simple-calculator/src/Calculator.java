public class Calculator {
    double number1;
    double number2;

    public void sum(){
        double sum2 = number1 + number2;
        System.out.println("Sum:" + sum2);
    }

    public void subtraction(){
        double subtraction2 = number1 - number2;
        System.out.println("Subtraction:" + subtraction2);
    }

    public void multiplication(){
        double multiplication2 = number1 * number2;
        System.out.println("Multiplication:" + multiplication2);
    }

    public void division(){
        double division2 = number1 / number2;
        System.out.println("Division:" + division2);
    }
}
